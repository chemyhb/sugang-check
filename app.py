# --- 매트릭스 형태로 체크박스 생성 ---
for category, sem_data in matrix_data.items():
    cols = st.columns([1.2, 2, 2, 2, 2])
    cols[0].markdown(f"**{category}**")
    
    for i, sem in enumerate(["2-1", "2-2", "3-1", "3-2"]):
        with cols[i+1]:
            if sem in sem_data:
                for group_id, subj, tag in sem_data[sem]:
                    # 예술, 교양 등 택1 과목은 노란 원으로 시각적 차이를 둠
                    marker = "🟡" if group_id in ["2-1-A", "2-2-C", "3-1-F", "3-2-H"] else "🔵"
                    label = f"{marker} [{tag}] {subj}"
                    
                    if st.checkbox(label, key=f"{group_id}_{subj}"):
                        selected_subjects.append(subj)
                        group_counts[group_id]['count'] += 1
                        selected_by_semester[semester_mapping[group_id]].append(f"[{tag}] {subj}")
    st.markdown("<hr style='margin: 10px 0; border: none; border-top: 1px solid #e6e6e6;'>", unsafe_allow_html=True)

