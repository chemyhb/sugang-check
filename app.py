@st.dialog("🔔 수강신청 검증 결과")
def show_result_dialog(errors):
    if errors:
        st.error(f"❌ 총 {len(errors)}건의 오류가 발견되었습니다. 창을 닫고 체크를 수정해 주세요.")
        for e in errors:
            st.warning(e)
    else:
        st.success("✅ 완벽합니다! 모든 졸업 필수 요건과 과목 위계 규정을 충족했습니다.")
        st.balloons()
        st.info("창을 닫고 화면 맨 아래의 '수강신청 내역 요약'을 확인한 후 시스템에 최종 제출해 주세요.")

